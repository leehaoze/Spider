    def __init__(self, name=None, analyst=None):
        super(BaseSpider, self).__init__(name)
        if analyst is not None:
            self.analyst = analyst
            self.allowed_domains = self.analyst.allowed_domains
            self.start_urls = self.analyst.start_urls
        elif getattr(self, 'analyst', None):
            self.allowed_domains = self.analyst.allowed_domains
            self.start_urls = self.analyst.start_urls
        elif not getattr(self, 'analyst', None):
            raise ValueError("%s must have a analyst" % type(self).__name__)

    def start_requests(self):
        for url in self.start_urls:
            yield self.splash_request(url=url, callback=self.parse)

    def parse(self, response):
        raise NotImplementedError

    def splash_request(
            self, url=None,
            callback=None,
            method='GET',
            endpoint='render.html',
            args=None,
            splash_url=None,
            slot_policy=SlotPolicy.PER_DOMAIN,
            splash_headers=None,
            dont_process_response=False,
            dont_send_headers=False,
            magic_response=True,
            session_id='default',
            http_status_from_error_code=True,
            cache_args=None,
            meta=None, ):
        headers = {'User-Agent': random.choice(self.user_agent_list)}
        return SplashRequest(url, callback, method, endpoint, args, splash_url, slot_policy, headers,
                             dont_process_response,
                             dont_send_headers, magic_response, session_id, http_status_from_error_code, cache_args,
                             meta)
